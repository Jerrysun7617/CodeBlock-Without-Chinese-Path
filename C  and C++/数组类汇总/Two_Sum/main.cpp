#include <iostream>
#include <vector>
using namespace std;
class Solution {
public:
    vector<int> twoSum(vector<int>& nums, int target) {
        vector<int> test;
        vector<int>::iterator iter,inner;


        int i,j;
        for(i = 0, iter = nums.begin(); iter < nums.end(); i++, iter++){

            for(j = i+1, inner = iter+1;inner<nums.end();j++,inner++){
                //cout<<"iter = "<<*iter<<"; "<<" inner "<<*inner<<endl;
                if((*iter + *inner )== target){
                    test.push_back(i);
                    test.push_back(j);
                    return test;
                }
            }
        }
        return test;
    }
};
